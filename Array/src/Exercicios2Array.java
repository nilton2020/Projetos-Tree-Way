
public class Exercicios2Array {

	public static void main(String[] args) {
		
		double [] array1 = {0.1,1.8,5,5};
		String [] array2 = {"a","b","c","teste"};
		
		for (double valorArray : array1) {
			
			System.out.println(valorArray);
		}
		
		for (String valorString : array2) {
			System.out.println(valorString);
			
		}
			
		
			
			
		
		

	}

}
